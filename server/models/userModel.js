import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true
    },

    email: {
      type: String,
      required: true
    },

    passwordHash: {
      type: String,
      required: true
    },

    isActive: {
      type: Boolean,
      default: true
    },

    refreshToken: {
      type: String
    }
  },
  { timestamps: true }
);

export default mongoose.model("User", userSchema);
